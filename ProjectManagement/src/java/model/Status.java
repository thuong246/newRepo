package model;

/**
 *
 * @author Admin
 */
public class Status {
    private String id;
    private String status;
    private String app_id;

    public Status() {
    }

    public Status(String id, String status, String app_id) {
        this.id = id;
        this.status = status;
        this.app_id = app_id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getApp_id() {
        return app_id;
    }

    public void setApp_id(String app_id) {
        this.app_id = app_id;
    }
    
    
    
}
